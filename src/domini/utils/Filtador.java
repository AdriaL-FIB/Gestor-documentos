    /**
     * Donades un seguit de paraules mira quines no són stopWords i les retorna.
     * @return array d'enters, retorna el {@code String[]} d'entrada sense les stop words.
     */
    public static String[] filtrarStopWords(String[] paraules) {
        return Arrays.stream(paraules).filter(p -> !stopWords.contains(p)).toArray(String[]::new);
    }

    /**
     * Separa el contingut en paraules i les retorna.
     * @param contingut -> String; contingut a separar.
     * @param regex -> String; expressió regular que indica com s'ha de separar el {@code contingut}.
     * @return array d'enters, retorna el contingut separat per l'expressió regular {@code regex}, traient els espais no útils i Strings buits.
     */
    public static String[] filtrarContingut(String contingut, String regex) {
        String[] paraules = contingut.split(regex);
        //Elimina espacios inutiles y palabras vacias
        //"      p       " --> "p"
        paraules = Arrays.stream(paraules).filter(p -> !p.equals("")).map(String::strip).toArray(String[]::new);
        return paraules;
    }

    /**
     * Funciona igual que filtrarStopWords pero li entra un map de paraules.
     * @param  map -> Map<String, Pair<Double, Double>>; map que conté com a claus primaries paraules.
     * @return array d'enters, retorna un {@code String[]} amb les paraules que no són stopWords (tenen el first del pair diferent de -1).
     */
    public static String[] filtrarMapStopWords(Map<String, Pair<Double, Double>> map) {
        return map.entrySet().stream().filter(p -> p.getValue().getFirst() != -1d).map(Map.Entry::getKey).toArray(String[]::new);
    }
